package estudar.marco.variaveisTipos;

public class TiposVariaveisConcatenacao {
    public static void main(String[] args) {
        System.out.println(1+1+1+ "1" + (1+1));
        System.out.println(1+1+"1"+1+1);
        System.out.println(1+"1"+1+"1");
        System.out.println("1"+1+1+1);
        System.out.println("1"+(1+1+1));
    }

    
}
